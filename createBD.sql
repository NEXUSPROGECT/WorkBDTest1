use Paper_Accounting;

create table tiker(
id integer primary key,
name_tiker varchar(30),
deal_place varchar(20)
);

create table fin_asset(
id integer primary key,
tiker_id integer,
registration varchar(50),
data_registration date,
emitent varchar(30),
form_issue varchar(20),
principal varchar(20),
amount integer,
foreign key (tiker_id) references tiker(id)
);

create table bond(
id integer primary key,
fin_asset_Id integer,
Data_Repayment date,
coupons_amount integer,
coupons_rate float4,
foreign key (fin_asset_id) references fin_asset(id)
);

create table securit(
id integer primary key,
fin_asset_Id integer,
Date_Accommodation date,
Date_Report date,
foreign key (fin_asset_id) references fin_asset(id) 
);

